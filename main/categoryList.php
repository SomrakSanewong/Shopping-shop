<script>
    document.addEventListener("DOMContentLoaded", () => {
        const filterButtons = document.querySelectorAll(".product-filters ul li");
        const products = document.querySelectorAll(".product-item");
        const viewAllBtn = document.querySelector(".view-all-btn");

        if (viewAllBtn) {
            setTimeout(() => viewAllBtn.classList.add("show"), 300);
        }

        filterButtons.forEach(btn => {
            btn.addEventListener("click", () => {
                filterButtons.forEach(b => b.classList.remove("filter-active"));
                btn.classList.add("filter-active");

                const filterValue = btn.getAttribute("data-filter").substring(1);

                products.forEach(item => {
                    if (filterValue === "all" || item.classList.contains(filterValue)) {
                        item.style.display = "block";
                    } else {
                        item.style.display = "none";
                    }
                });

                if (viewAllBtn) {
                    if (filterValue === "all") {
                        setTimeout(() => viewAllBtn.classList.add("show"), 400);
                    } else {
                        viewAllBtn.classList.remove("show");
                    }
                }
            });
        });
    });
</script>